using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TheGeneralStore.Frontend.ConsoleClient
{
    public class Maths
    {
        public int add(int t1, int t2)
        {
            return t1 + t2;
        }

        public int multiply(int t1, int t2)
        {
            return t1 * t2;
        }

        public int subtraction(int t1, int t2)
        {
            return t1 - t2;
        }
    }
}
